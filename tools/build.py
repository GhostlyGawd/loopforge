def parse_front_matter(text):
    parts = text.split("---", 2)
    meta = {}
    if len(parts) < 3:
        return meta, text
    for line in parts[1].strip().splitlines():
        if ":" not in line or line.strip().startswith("#"):
            continue
        key, _, raw = line.partition(":")
        key, raw = key.strip(), raw.split(" #")[0].strip()
        if raw.startswith("[") and raw.endswith("]"):
            inner = raw[1:-1].strip()
            meta[key] = [v.strip() for v in inner.split(",") if v.strip()] if inner else []
        else:
            meta[key] = raw
    return meta, parts[2]


def first_paragraph(body):
    lines, out, seen_h1 = body.splitlines(), [], False
    for line in lines:
        if line.startswith("# "):
            seen_h1 = True
            continue
        if seen_h1:
            if line.startswith("#"):
                break
            if line.strip():
                out.append(line.strip())
            elif out:
                break
    return " ".join(out)


def collect():
    entries = []
    for path in sorted(LOOPS.rglob("LP-*.md")):
        meta, body = parse_front_matter(path.read_text())
        meta["purpose"] = first_paragraph(body)
        meta["path"] = str(path.relative_to(ROOT))
        entries.append(meta)
    return entries


def build_index(entries, tax, state):
    cats = {c["id"]: c for c in tax["categories"]}
    name = state.get("name") or f"{state.get('codename', 'loopforge')} (pre-brand)"
    lines = [
        "<!-- GENERATED by tools/build.py — do not edit by hand -->",
        f"# {name} — Library Index",
        "",
        f"{len(entries)} loops · iteration {state.get('iteration', 0)} · phase {state.get('phase', '?')}",
        "",
    ]
    for cat_id, cat in cats.items():
        group = [e for e in entries if e.get("category") == cat_id]
        lines.append(f"## {cat['name']} — {cat['desc']}")
        if not group:
            lines.append("_empty — a gap the Scout should notice_")
        for e in group:
            tags = ", ".join(e.get("tags", []))
            lines.append(
                f"- **[{e['id']} · {e['title']}]({e['path'].replace('library/', '')})** "
                f"`{e.get('tier', '?')}` `{e.get('status', '?')}` — {e.get('purpose', '')[:140]} "
                f"_(tags: {tags})_"
            )
        lines.append("")
    (ROOT / "library" / "INDEX.md").write_text("\n".join(lines) + "\n")


