def build_site(entries, tax, state):
    title = state.get("name") or "UNNAMED"
    strap = (
        "A self-running library of loop prompts for Claude Code — built, reviewed, "
        "filed, and branded by the loop that maintains it."
    )
    data = [
        {
            "id": e.get("id", "?"),
            "title": html.escape(e.get("title", "?")),
            "category": e.get("category", "?"),
            "tier": e.get("tier", "?"),
            "status": e.get("status", "draft"),
            "tags": [html.escape(t) for t in e.get("tags", [])],
            "purpose": html.escape(e.get("purpose", "")),
            "stop_when": html.escape(e.get("stop_when", "")),
            "path": e.get("path", ""),
        }
        for e in entries
    ]
    chips = "\n    ".join(
        f'<button class="chip" data-cat="{c["id"]}" aria-pressed="false">{html.escape(c["name"])}</button>'
        for c in tax["categories"]
    )
    cats = {c["id"]: c["name"] for c in tax["categories"]}
    page = (
        TEMPLATE
        .replace("@@TITLE@@", html.escape(title))
        .replace("@@ITER@@", str(state.get("iteration", 0)).zfill(3))
        .replace("@@PHASE@@", html.escape(str(state.get("phase", "?"))))
        .replace("@@COUNT@@", str(len(entries)).zfill(3))
        .replace("@@STRAP@@", strap)
        .replace("@@CHIPS@@", chips)
        .replace("@@DATA@@", json.dumps(data))
        .replace("@@CATS@@", json.dumps(cats))
        .replace("@@STAMP@@", datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%MZ"))
    )
    (ROOT / "site" / "index.html").write_text(page)


def main():
    tax = json.loads((ROOT / "library" / "categories.json").read_text())
    state = json.loads((ROOT / "state" / "STATE.json").read_text())
    entries = collect()
    build_index(entries, tax, state)
    build_site(entries, tax, state)
    print(f"BUILD: OK — INDEX.md + site/index.html ({len(entries)} entries)")


if __name__ == "__main__":
    main()
